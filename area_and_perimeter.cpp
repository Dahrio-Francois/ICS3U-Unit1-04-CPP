// This program calculates the area and perimeter of a rectangle
//     with the dimensions 6cm x 7cm

#include <iostream>

int main() {
    std::cout << "If a rectangle has the dimensions" << std::endl;
    std::cout << "6cm x 7cm" << std::endl;
    std::cout << "" << std::endl;
    std::cout << "Area is " << (6*7) << "cm^2" << std::endl;
    std::cout << "Perimeter is " << (2*(6+7)) << "cm" << std::endl;
}